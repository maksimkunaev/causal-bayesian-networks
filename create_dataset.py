# Function to convert string values to numeric format
def convert_to_numeric(cases):
    """
    Converts string values to numeric format for Bayesian Network

    Mapping:
    - founder_experience: Yes=1, No=0
    - market_timing: Good=1, Bad=0
    - funding: Yes=1, No=0
    - team_size: Small=0, Medium=1, Large=2
    - success: Yes=1, No=0
    """
    mapping = {
        "founder_experience": {"Yes": 1, "No": 0},
        "market_timing": {"Good": 1, "Bad": 0},
        "funding": {"Yes": 1, "No": 0},
        "team_size": {"Small": 0, "Medium": 1, "Large": 2},
        "success": {"Yes": 1, "No": 0},
    }

    numeric_data = []
    for case in cases:
        numeric_case = {}
        for field, value in case.items():
            numeric_case[field] = mapping[field][value]
        numeric_data.append(numeric_case)

    return pd.DataFrame(numeric_data)


